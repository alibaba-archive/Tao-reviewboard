package com.taobao.eclipse.plugin.reviewboard.core.model;

import java.io.Serializable;

import org.json.JSONObject;

import com.taobao.eclipse.plugin.reviewboard.core.util.JsonUtil;

/**
 * 类说明:user
 *
 * @author 智清 
 * 创建时间：2010-11-8
 */
public class ModelUser implements ModelConverter, Serializable {
    
    private static final long serialVersionUID = -2293039207891523007L;

    private int id;
    
    private String username;
    
    private String firstName;
    
    private String lastName;
    
    private String url;
    
    private String email;
    
    public void convertToModel(JSONObject jsonObject) {
        try {
            id  = JsonUtil.getIntFromJsonObject(jsonObject, "id") ;
            url = JsonUtil.getStringFromJsonObject(jsonObject, "url");
            username = JsonUtil.getStringFromJsonObject(jsonObject, "username");
            email = JsonUtil.getStringFromJsonObject(jsonObject, "email");
            firstName = JsonUtil.getStringFromJsonObject(jsonObject, "first_name");
            lastName = JsonUtil.getStringFromJsonObject(jsonObject, "last_name");
        } catch (Exception e) {
            throw new RuntimeException(e);
        }
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getFirstName() {
        return firstName;
    }

    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    public String getLastName() {
        return lastName;
    }

    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((email == null) ? 0 : email.hashCode());
        result = prime * result + ((firstName == null) ? 0 : firstName.hashCode());
        result = prime * result + id;
        result = prime * result + ((lastName == null) ? 0 : lastName.hashCode());
        result = prime * result + ((url == null) ? 0 : url.hashCode());
        result = prime * result + ((username == null) ? 0 : username.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        ModelUser other = (ModelUser) obj;
        if (email == null) {
            if (other.email != null)
                return false;
        } else if (!email.equals(other.email))
            return false;
        if (firstName == null) {
            if (other.firstName != null)
                return false;
        } else if (!firstName.equals(other.firstName))
            return false;
        if (id != other.id)
            return false;
        if (lastName == null) {
            if (other.lastName != null)
                return false;
        } else if (!lastName.equals(other.lastName))
            return false;
        if (url == null) {
            if (other.url != null)
                return false;
        } else if (!url.equals(other.url))
            return false;
        if (username == null) {
            if (other.username != null)
                return false;
        } else if (!username.equals(other.username))
            return false;
        return true;
    }

}
